    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            CoeficientBinomial coeficientBinomial = new CoeficientBinomial();
            int n = Integer.parseInt(exponente.getText());
            int k = Integer.parseInt(numeroCoeficiente.getText()) - 1;
            if (n >= 0 && k>=0 && k<n+1) {
                long r1 = System.nanoTime();
                etiquetaRecursivo.setText(String.valueOf(coeficientBinomial.recursivo(n, k)));
                long r2 = System.nanoTime();
                long d1 = System.nanoTime();
                etiquetaDinamico.setText(String.valueOf(coeficientBinomial.dinamica(n, k)));
                long d2 = System.nanoTime();
                tiempoRecursividad.setText(String.valueOf((r2 - r1) + " nanoSegundos"));
                tiempoDinamica.setText(String.valueOf((d2 - d1) + " nanoSegundos"));
                cargarTabla(coeficientBinomial.dinamicaCompleta(n));
                jButton1.setVisible(false);
                jButton2.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "INGRESE VALORES VALIDOS \n INTENTE DE NUEVO");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "INGRESE VALORES VALIDOS \n INTENTE DE NUEVO");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

